// 获取字符点阵数据
const uint8_t* songti_font_get_char_data(char c) {
    if (c >= SONGTI_FONT_START_CHAR && c <= SONGTI_FONT_END_CHAR) {
        return songti_font_10x13[c - SONGTI_FONT_START_CHAR];
    }
    return songti_font_10x13[0]; // 返回空格字符
}